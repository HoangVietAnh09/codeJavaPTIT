public class Phong {
    private String MP, tenPhong;

    public Phong(String MP, String tenPhong) {
        this.MP = MP;
        this.tenPhong = tenPhong;
    }

    public String getTenPhong(){
        return tenPhong;
    }
    public String getMP() {
        return MP;
    }


}
