package codePTIT;

import java.util.Scanner;

public class J03004 {
	public static Scanner sc = new Scanner(System.in);
	public static void main(String[] args) {
		int t = sc.nextInt();
		while(t-->0) {
			String s = sc.nextLine();
			s = s.trim();
			s = s.toLowerCase();
			System.out.println(s);
			
		}
		
			
	}

}
